    public function setParamsByArray($book) {
        foreach ($book as $key => $value) {
            if (property_exists($this, $key)) {
                $this->$key = $value;
            }
        }

        if ($this->payment != null) {
            $payment = new Payment();
            $payment->setParamsByArray($this->payment);
            $this->payment = $payment;
        }
    }

    public function getParamsAsArray() {
        $array = get_class_vars("Book");

        foreach ($array as $key => $value) {
            if ($this->existParam($key)) {
                $array[$key] = $this->$key;
            } else {
                unset($array[$key]);
            }
        }

        return $array;
    }

    public function existParam($name) {
        if (!is_array($this->$name)) {
            $existParam = $this->$name != null || $this->$name != '';
        } else {
            $existParam = false;
            foreach ($this->$name as $value) {
                $existParam = $existParam || $value != null || $value != '';
            }
        }
        return $existParam;
    }

